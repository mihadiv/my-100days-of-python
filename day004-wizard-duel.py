# Fire burns Earth -> Fire wins
# Earth absorbs Water -> Earth wins
# Water extinguishes Fire -> Water wins

user_choice = int(input("Your choice: "))

if user_choice < 0 or user_choice > 2:
    print("Invalid choice. Ypu lose!")
else:
    print(f"\nYou chose {names[user_choice]}:")
    print(elements[user_choice])

    computer_choice = random.randint(0, 2)
    print(f"\nComputer chose {names[computer_choice]}:")
    print(elements[computer_choice])

    if user_choice == computer_choice:
        print("\nIt's a draw!")
    elif (user_choice == 0 and computer_choice == 2) or \
            (user_choice == 1 and computer_choice == 0) or \
            (user_choice == 2 and computer_choice == 1):
        print("\nYou win the duel!")
    else:
        print("\nYou lose the duel!")
